    private void btn_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_createActionPerformed

        if(verifData()){
            try {
                Connection con = Connect.getInstance();
                PreparedStatement ps;
                ps = con.prepareStatement("INSERT INTO users(fname, lname, user_name,pass, picture, statut) VALUES (?,?,?,?,?,?)");
                ps.setString(1, txt_firstname.getText());
                ps.setString(2, txt_lastname.getText());
                ps.setString(3, txt_username.getText());
                ps.setString(4, String.valueOf(txt_password.getPassword()));
                InputStream img = new FileInputStream(new File(imagePath));
                ps.setBlob(5, img);
                ps.setString(6, combo_statut.getSelectedItem().toString());
                 
                if(isUsernameExist(txt_username.getText())){
                    JOptionPane.showMessageDialog(null, "ce nom d'utilisateur existe deja veillez choisir un autre ...");
                }
                else{
                    if(ps.executeUpdate() != 0){
                        JOptionPane.showMessageDialog(null, "Compte crée avec success ...");
                        refreshJtable();
                        clearField();
                    }else{
                        JOptionPane.showMessageDialog(null, "Quelque chose est incorrecte !");
                    }
                }
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_createActionPerformed

