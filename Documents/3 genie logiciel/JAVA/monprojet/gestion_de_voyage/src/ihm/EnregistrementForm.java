    public boolean verifData(){
       if(txt_firstname.getText().equals("")&& txt_lastname.getText().equals("")
                || txt_username.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Un ou plusieurs champs sont vide ...");
           return false;
       }else if(imagePath==null){
           JOptionPane.showMessageDialog(null, "Pas d'image selectionnée ...");
           return false;
       }
       else{
           return true;
       }
      
   }
    
   public boolean isUsernameExist(String un ){
       boolean uExist = false;
       
       try {
            Connection con = Connect.getInstance();
            PreparedStatement ps;
            ResultSet rs;
            
            ps = con.prepareStatement("SELECT * FROM `users` WHERE `user_name`=?");
            ps.setString(1, txt_username.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                uExist = true;
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Loginform.class.getName()).log(Level.SEVERE, null, ex);
        }
       return uExist;
   }
   
   public void populateJtable(){ 
        
        EnregistrementQuery cq = new EnregistrementQuery();
        ArrayList<User> ctList = cq.contactList();
        String[] colNames = {"Id" ,"First name", "Laste name", "User name", "Image" };
        
        Object[][]rows = new Object[ctList.size()][8];
        
        for(int i = 0; i<ctList.size(); i++){
            rows[i][0]=ctList.get(i).getId();
            rows[i][1]=ctList.get(i).getFname();
            rows[i][2]=ctList.get(i).getLname();
            rows[i][3]=ctList.get(i).getUse_name();
        
        
            ImageIcon pic = new ImageIcon(new ImageIcon
                                         (ctList.get(i).getPic())
                                         .getImage()
                                         .getScaledInstance(120, 80, Image.SCALE_SMOOTH));
        
        rows[i][4] = pic;
        }
        
        MyModel1 mmd = new MyModel1 ( colNames, rows );
        jTable1.setModel(mmd);
        jTable1.setRowHeight(80);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(120);
    }
    
   public void refreshJtable(){
        jTable1.setModel(new DefaultTableModel());
        populateJtable();
    }
    
