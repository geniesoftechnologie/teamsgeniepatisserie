    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{  
            JasperDesign jd = JRXmlLoader.load("report\\reportClient.jrxml");
            String sql = "SELECT\n"+
                                     "utilisateur.`id` AS utilisateur_id,\n"+
                                     "utilisateur.`nom` AS utilisateur_nom,\n"+
                                     "utilisateur.`prenom` AS utilisateur_prenom,\n"+
                                     "utilisateur.`sexe` AS utilisateur_sexe,\n"+
                                     "utilisateur.`dateNaissance` AS utilisateur_dateNaissance,\n"+
                                     "utilisateur.`telephone` AS utilisateur_telephone,\n"+
                                     "utilisateur.`email` AS utilisateur_email,\n"+
                                     "utilisateur.`codePieceIdent` AS utilisateur_codePieceIdent\n"+
                                "FROM\n"+
                                     "`utilisateur` utilisateur\n";
            
            System.out.println("Query");
            
            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(sql);
            jd.setQuery(newQuery);
            net.sf.jasperreports.engine.JasperReport jasperReport = JasperCompileManager.compileReport(jd);
            JasperPrint jasperprint = JasperFillManager.fillReport(jasperReport,null, Connect.getInstance());
            JasperViewer.viewReport(jasperprint, false);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Messege", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

