    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        int index = jTable1.getSelectedRow();
        if(index >= 0){
            int id = (int) jTable1.getValueAt(index, 0);
            UtilisateurDao rdao = new UtilisateurDao();
            rdao.supprimer(id);
            UtilisateurDao cdao = new UtilisateurDao();
            cdao.supprimer(id);
            JOptionPane.showMessageDialog(null, "Client supprimé...");
            vider();
            list_res();
        }
        else{
            JOptionPane.showMessageDialog(this, "Veuillez selectionner une ligne svp...");
        }
    }//GEN-LAST:event_btn_delActionPerformed

