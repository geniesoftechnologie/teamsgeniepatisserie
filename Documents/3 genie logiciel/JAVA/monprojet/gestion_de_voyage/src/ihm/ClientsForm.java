private void list_res(){
    UtilisateurDao rdao = new UtilisateurDao();
    List<Utilisateur> lr = rdao.liste();
    for(int i =0; i<lr.size(); i ++){
        ((DefaultTableModel) jTable1.getModel()).addRow(new Object []{
            lr.get(i).getId(),
            lr.get(i).getNom(),
            lr.get(i).getPrenom(),
            lr.get(i).getDatenaissance(),
            lr.get(i).getSexe(),
            lr.get(i).getTelephone(),
            lr.get(i).getEmail(),
            lr.get(i).getCodepieceident()
        }
                
                );
    }
    }

private void vider(){
      txt_nom.setText("");
      txt_prenom.setText("");
      combo_genre.setSelectedItem(null);
      txt_datenaiss.setDate(null);
      txt_phone.setText("");
      txt_email.setText("");
      txt_passport.setText("");
      
      
      DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
      int m = model.getRowCount();
      for(int i = m-1; i>=0; --i){
          model.removeRow(i);
      }
      
  } 
