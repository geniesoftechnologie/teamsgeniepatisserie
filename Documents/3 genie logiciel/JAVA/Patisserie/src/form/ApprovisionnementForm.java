    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        int ids = (int) jTable1.getValueAt(index, 0);
        ProduitDao cdao = new ProduitDao();
        Produit c = cdao.rechercher(ids);
        if(c!=null){
            txt_idp.setText(String.valueOf(c.getIdp()));
            cat_combo.setSelectedItem(c.getCategorie());
            txt_desi.setText(c.getDesignation());
            txt_pu.setText(String.valueOf(c.getPu())); 
            
            }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        int index = jTable1.getSelectedRow();
        if(index >= 0){
            int idp = (int) jTable1.getValueAt(index, 0);
            ApprovisionnementDao apdao = new ApprovisionnementDao();
            apdao.supprimer(idp);
            vider();
            list_cat();
        }
        else{
            JOptionPane.showMessageDialog(this, "Veuillez selectionner une ligne svp...");
        }
    }//GEN-LAST:event_btn_delActionPerformed

    private void btn_edActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edActionPerformed
        int index = jTable1.getSelectedRow();
        int idp = (int) jTable1.getValueAt(index, 0);
        ApprovisionnementDao cdao = new ApprovisionnementDao();
        StockDao sdao = new StockDao();
        String cat = cat_combo.getSelectedItem().toString();
        String desi = txt_desi.getText();
        String pu = txt_pu.getText();
        String qte = txt_qte.getText();
        Date dateap = txt_date.getDate();
        if(cat.equals("") || desi.equals("") || pu.equals("")){
            JOptionPane.showMessageDialog(this, "Veuillez cliquer sur la ligne ou modifier...");
        }
        else {
            Approvisionnement ap = new Approvisionnement(cat, desi, Integer.parseInt(pu),Integer.parseInt(qte), dateap);
            cdao.modifier(ap, idp);

            Stock st = new Stock(idp, cat, desi, Integer.parseInt(pu), Integer.parseInt(qte));
            sdao.modifier(st, idp);
            vider();
            list_cat();
        }
    }//GEN-LAST:event_btn_edActionPerformed

