private void list_cat(){
     ProduitDao cdao = new ProduitDao();
    List<Produit> lcat = cdao.liste();
    for(int i =0; i<lcat.size(); i ++){
        ((DefaultTableModel) jTable1.getModel()).addRow(new Object []{
            lcat.get(i).getIdp(),
            lcat.get(i).getCategorie(),
            lcat.get(i).getDesignation(),
            lcat.get(i).getPu()
        }
                
                );
    }
    }

private void list_pro(){
     ApprovisionnementDao cdao = new ApprovisionnementDao();
    List<Approvisionnement> lcat = cdao.liste();
    for(int i =0; i<lcat.size(); i ++){
        ((DefaultTableModel) jTable2.getModel()).addRow(new Object []{
            lcat.get(i).getIds(),
            lcat.get(i).getIdp(),
            lcat.get(i).getCategorie(),
            lcat.get(i).getDesignation(),
            lcat.get(i).getPu(),
            lcat.get(i).getQuantite(),
            lcat.get(i).getDateap()
        }
                
                );
    }
    }
  private void vider(){
      txt_desi.setText("");
      txt_pu.setText("");
      DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
      int n = model.getRowCount();
      for(int i = n-1; i>=0; --i){
          model.removeRow(i);
      }
      
  }
    
